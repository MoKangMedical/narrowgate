def get_day(day: int) -> Dict:
    """返回单天课程详情（字典格式）"""
    course = get_course_by_day(day)
    if not course:
        return None
    return {
        "day": course.day,
        "title": course.title,
        "theme": course.theme,
        "teaching": course.teaching,
        "challenge": course.challenge,
        "reflection_questions": course.reflection_questions,
        "guiding_prompt": course.guiding_prompt,
        "golden_quote": course.golden_quote,
        "difficulty": course.difficulty,
        "recommended_master": course.recommended_master,
        "exp_reward": course.exp_reward,
        "week": course.week,
        "dimension": course.dimension,
        "evolution_target": course.evolution_target,
        "evasion_detection": course.evasion_detection,
        "theory_reference": course.theory_reference,
    }


def get_week(week: int) -> List[Dict]:
    """返回指定周的全部课程概览"""
    return [
        {"day": c.day, "title": c.title, "theme": c.theme}
        for c in COURSE_CONTENT if c.week == week
    ]
