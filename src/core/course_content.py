# 查询函数
# ============================================================

def get_day(day: int) -> Optional[dict]:
    """获取单天课程"""
    if 1 <= day <= 30:
        return COURSE_DAYS[day - 1]
    return None


def get_week(week: int) -> list:
    """获取整周课程"""
    return [d for d in COURSE_DAYS if d["week"] == week]


def get_all_days() -> list:
    """获取全部30天课程概览（不含完整teaching）"""
    return [
        {
            "day": d["day"],
            "title": d["title"],
            "theme": d["theme"],
            "week": d["week"],
            "difficulty": d["difficulty"],
            "master_id": d["master_id"],
            "xp": d["xp"],
        }
        for d in COURSE_DAYS
    ]


def get_day_range(start: int, end: int) -> list:
    """获取天数范围"""
    return [d for d in COURSE_DAYS if start <= d["day"] <= end]
